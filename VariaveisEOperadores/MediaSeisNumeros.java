public class MediaSeisNumeros{
    
    //Media de seis numeros
    public static void main(String[] args){
        float notaA = 6.5f;
        float notaB = 7.3f;
        float notaC = 10.0f;
        float notaD = 4.6f;
        float notaE = 8.6f; 
        float notaF = 2.4f;

        float media;
        media = (notaA + notaB + notaC + notaD + notaE + notaF)/6;

        System.out.println("Media: " + media);
    }
}